        if(chkTravel.isSelected()){
            txtlstHobby.append("旅行\n");
        }
        if(chkShopping.isSelected()){
            txtlstHobby.append("ショッピング\n");
        }
        if(chkReadBook.isSelected()){
            txtlstHobby.append("読書\n");
        }
        if(chkSport.isSelected()){
            txtlstHobby.append("スポーツ\n");
        }
        
        
        
        
        
