    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
    try {
    File file = new File(Address);
    FileReader fr = new FileReader(file);
    BufferedReader br = new BufferedReader(fr) ;
    String line;
    DefaultListModel listModel = new DefaultListModel(); //tao doi tuong dinh dang list
                lstAddress.setModel(listModel); //cai dat dinh dang list cho listAddress
        
            while((line = br.readLine()) != null){
                listModel.addElement(line);   
            }  
    } catch (IOException ex) {
            Logger.getLogger(FrmQuestionnaire.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowActivated

