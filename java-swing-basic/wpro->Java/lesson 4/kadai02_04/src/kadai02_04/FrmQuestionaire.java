    private void WriterFile(String line,String Wfile) throws IOException{
        File file = new File(Wfile);
        if(!file.exists()){
             file.createNewFile();
        }
        try (FileWriter fw = new FileWriter(file,true); BufferedWriter bw = new BufferedWriter(fw)) {
            
            bw.write(line);
            bw.newLine();
            bw.flush();
            
        }
        
    }

