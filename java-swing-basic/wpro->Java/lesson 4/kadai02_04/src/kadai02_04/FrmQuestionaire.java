    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
       String line;
        
        if(rdoMen.isSelected()){ //isSelected
            line = "男 ";
        }
        else{
            line = "女 ";
        }
        int x = lstAddress.getSelectedIndex();
        if(x != -1){
             line += lstAddress.getSelectedValue(); //getSelectedValue   
        }
        else{
            JOptionPane.showMessageDialog(null,"please! select item","Error", JOptionPane.ERROR_MESSAGE);
        }
        int y = cmbjob.getSelectedIndex();
        if(y != -1){
            line += cmbjob.getSelectedItem().toString();  //getSelectedItem().toString()
        }
        else{
            JOptionPane.showMessageDialog(null,"please! select item","Error", JOptionPane.ERROR_MESSAGE);
        }
        
        if(chkMusic.isSelected()){
            line += " 音楽鑑賞 :" ;  //append viet them vao
        }
        if(chkMovie.isSelected()){
            line += " 映画鑑賞 :";
        }
        if(chkTravel.isSelected()){
            line += " 旅行 :";
        }
        if(chkShopping.isSelected()){
            line += " ショッピング :";
        }
        if(chkReadBook.isSelected()){
            line += " 読書 :";
        }
        if(chkSport.isSelected()){
            line += " スポーツ :";
        }
        String line2 = line.substring(0,(line.length() - 2));
        
        try {
            WriterFile(line2,NEWFILE);
            JOptionPane.showMessageDialog(null, "Write Success");
        } catch (IOException ex) {
            Logger.getLogger(FrmQuestionaire.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnOkActionPerformed

