    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!(txtStudentNo.getText()).equals("")){
            Myconnect con = new Myconnect();
            con.connect();
            ResultSet ret = con.getDataStudentName(txtStudentNo.getText());
            try {
                if(ret.next()){
                    txtStudentName.setText(ret.getString(1));
                    
                    
                    
                    x = true;
                    checkbutton(x,y);
                }
                else{
                    JOptionPane.showMessageDialog(null,"該当がありません", "入力エラー",JOptionPane.ERROR_MESSAGE);
                    txtStudentName.setText("");
                    x = false;
                    checkbutton(x,y);
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrmKashidashi.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtIsbnKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIsbnKeyPressed
        // TODO add your handling code here:
                if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!txtIsbn.getText().equals("")){
            Myconnect con = new Myconnect();
            con.connect();
            //入力したコードが正しいかどうかをチェックする
            ResultSet ret = con.getSearchBook(txtIsbn.getText());
            try {
                if(ret.next()){
                    
                    //借りている本かどうかをチェックする
                    ResultSet ret1 = con.getDataKashiBook(txtIsbn.getText());
                    if(!ret1.next()){
                        txtTitle.setText(ret.getString(1));
                        y = true;
                        checkbutton(x,y);
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "貸し出している本です", "エラー",JOptionPane.ERROR_MESSAGE);
                        txtTitle.setText("");
                        y = false;
                        checkbutton(x,y);
                    }
                   
                }
                else{
                    JOptionPane.showMessageDialog(null, "該当がありません", "エラー",JOptionPane.ERROR_MESSAGE);
                    txtTitle.setText("");
                        y = false;
                        checkbutton(x,y);
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrmHenkyaku.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            }
            else{
                    JOptionPane.showMessageDialog(null, "未入力の項目があります", "エラー",JOptionPane.ERROR_MESSAGE);
                    txtTitle.setText("");
                        y = false;
                        checkbutton(x,y);
            }
        }
    }//GEN-LAST:event_txtIsbnKeyPressed

