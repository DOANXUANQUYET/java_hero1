    private void txtIsbnKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIsbnKeyPressed
        // TODO add your handling code here:
        
        //空白が入力しないように　チェックする
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!txtIsbn.getText().equals("")){
            Myconnect con = new Myconnect();
            con.connect();
            //入力したコードが正しいかどうかをチェックする
            ResultSet ret = con.getSearchBook(txtIsbn.getText());
            try {
                if(ret.next()){
                    
                    //借りている本かどうかをチェックする
                    ResultSet ret1 = con.getDataKashiBook(txtIsbn.getText());
                    if(ret1.next()){
                        txtTitle.setText(ret1.getString(1));
                        btnHenkyaku.setEnabled(true);
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "借りていない本です", "エラー",JOptionPane.ERROR_MESSAGE);
                        txtTitle.setText("");
                        btnHenkyaku.setEnabled(false);
                    }
                   
                }
                else{
                    JOptionPane.showMessageDialog(null, "該当がありません", "エラー",JOptionPane.ERROR_MESSAGE);
                    txtTitle.setText("");
                    btnHenkyaku.setEnabled(false);
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrmHenkyaku.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            }
            else{
                    JOptionPane.showMessageDialog(null, "未入力の項目があります", "エラー",JOptionPane.ERROR_MESSAGE);
                    txtTitle.setText("");
                    btnHenkyaku.setEnabled(false);
            }
        }

    }//GEN-LAST:event_txtIsbnKeyPressed

    private void btnHenkyakuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHenkyakuActionPerformed
        // TODO add your handling code here:
        Myconnect con = new Myconnect();
        con.connect();
        try {
            int rs = con.updateHendate(txtIsbn.getText());
            if(rs > 0){
                JOptionPane.showMessageDialog(null, "返却　成功","お知らせ",JOptionPane.INFORMATION_MESSAGE);
                txtIsbn.setText("");
                txtTitle.setText("");
                btnHenkyaku.setEnabled(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "返却 失敗","エラー",JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmHenkyaku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnHenkyakuActionPerformed

